DECLARE

  CURSOR EMP_CUR IS
  SELECT * 
  FROM EMPLOYEES
  WHERE DEPARTMENT_ID=90;
  
  emp_rec EMPLOYEES%ROWTYPE;
  
BEGIN
 OPEN EMP_CUR;
  LOOP
  
    FETCH EMP_CUR INTO EMP_REC;
    
    DBMS_OUTPUT.PUT_LINE(EMP_REC.FIRST_NAME);
    
    IF EMP_CUR%NOTFOUND THEN
    EXIT;
    END IF;
      
  END LOOP;

  null;
END;
/